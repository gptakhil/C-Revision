#include <iostream>

using namespace std;

int main(){
    int int_value = 13.9;
    // Initializes a variable of char data type
    char character = 'A';
    // Declares a variable of int type
    int ASCII;
    // Converts char data type into int explicitly
    ASCII = (int) character;
    // Prints value of variable
    cout << "ASCII value = " << ASCII;
    cout << int_value;
}
